function die([string] $message, [Int32] $code)
{
    if (-not $? -or $LASTEXITCODE)
    {
        if ($message)
        {
            "! --------------------------------------"
            "!   $message"
            "! --------------------------------------"
        }

        $res = if ($code) {$code} else {$LASTEXITCODE}

        "exit $res"

        exit $res
    }
}

function bye([Int32] $code)
{
    $res = if ($PSBoundParameters.ContainsKey('code')) {$code} else {$LASTEXITCODE}

    if ($res)
    {
        "`n  FAIL`n ======`n"
        exit $res
    }
    else
    {
        "`n  SUCCESS`n =========`n"
        exit 0
    }
}

function run_cmd([string] $cmd_line)
{
    "---------------------"
    "> $cmd_line"
    "---------------------"

    & $env:comspec /c $cmd_line
}

function run_app([string] $app, [string] $arguments, [boolean] $silent = $false)
{
    if (-not $silent)
    {
        "---------------------"
        "> ""$app"" $arguments"
        "---------------------"
    }

    $proc = start-process $app $arguments -nonewwindow -passthru -wait
    $proc.WaitForExit()
    $global:LASTEXITCODE = $proc.ExitCode
}

function CopyFiles([string[]] $files, [string] $dir)
{
    mkdir $dir

    die "Unable to create directory: $dir"

    foreach ($f in $files)
    {
        if ($f)
        {
            copy $f $dir

            die "Unable to copy $f to $dir"
        }
    }
}

